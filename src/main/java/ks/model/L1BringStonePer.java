package ks.model;

public class L1BringStonePer {
    private int dark;
    private int brave;
    private int wise;
    private int kayser;

    public L1BringStonePer(int dark, int brave, int wise, int kayser) {
        this.dark = dark;
        this.brave = brave;
        this.wise = wise;
        this.kayser = kayser;
    }

    public int getDark() {
        return dark;
    }

    public void setDark(int dark) {
        this.dark = dark;
    }

    public int getBrave() {
        return brave;
    }

    public void setBrave(int brave) {
        this.brave = brave;
    }

    public int getWise() {
        return wise;
    }

    public void setWise(int wise) {
        this.wise = wise;
    }

    public int getKayser() {
        return kayser;
    }

    public void setKayser(int kayser) {
        this.kayser = kayser;
    }
}
