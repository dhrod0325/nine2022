package ks.core.datatables.favPoly;

public class FavPoly {
    private int charId;
    private int polyId;
    private int idx;

    public int getCharId() {
        return charId;
    }

    public void setCharId(int charId) {
        this.charId = charId;
    }

    public int getPolyId() {
        return polyId;
    }

    public void setPolyId(int polyId) {
        this.polyId = polyId;
    }

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }
}
