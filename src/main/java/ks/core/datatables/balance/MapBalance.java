package ks.core.datatables.balance;

public class MapBalance {
    private int mapId;
    private String mapName;
    private double hpLeverage;
    private double mpLeverage;
    private double hitLeverage;
    private double dmgLeverage;
    private double magicHitLeverage;
    private double magicDmgLeverage;

    public int getMapId() {
        return mapId;
    }

    public void setMapId(int mapId) {
        this.mapId = mapId;
    }

    public String getMapName() {
        return mapName;
    }

    public void setMapName(String mapName) {
        this.mapName = mapName;
    }

    public double getHpLeverage() {
        return hpLeverage;
    }

    public void setHpLeverage(double hpLeverage) {
        this.hpLeverage = hpLeverage;
    }

    public double getMpLeverage() {
        return mpLeverage;
    }

    public void setMpLeverage(double mpLeverage) {
        this.mpLeverage = mpLeverage;
    }

    public double getHitLeverage() {
        return hitLeverage;
    }

    public void setHitLeverage(double hitLeverage) {
        this.hitLeverage = hitLeverage;
    }

    public double getDmgLeverage() {
        return dmgLeverage;
    }

    public void setDmgLeverage(double dmgLeverage) {
        this.dmgLeverage = dmgLeverage;
    }

    public double getMagicHitLeverage() {
        return magicHitLeverage;
    }

    public void setMagicHitLeverage(double magicHitLeverage) {
        this.magicHitLeverage = magicHitLeverage;
    }

    public double getMagicDmgLeverage() {
        return magicDmgLeverage;
    }

    public void setMagicDmgLeverage(double magicDmgLeverage) {
        this.magicDmgLeverage = magicDmgLeverage;
    }
}
