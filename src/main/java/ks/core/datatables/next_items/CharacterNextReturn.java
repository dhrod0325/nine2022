package ks.core.datatables.next_items;

public class CharacterNextReturn {
    private int itemId;
    private int enchantLevel;
    private int bless;
    private String itemName;
    private int returnItemId;
    private int returnItemCount;
    private int returnItemEnchant;
    private int returnItemBless;

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    public int getEnchantLevel() {
        return enchantLevel;
    }

    public void setEnchantLevel(int enchantLevel) {
        this.enchantLevel = enchantLevel;
    }

    public int getBless() {
        return bless;
    }

    public void setBless(int bless) {
        this.bless = bless;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public int getReturnItemId() {
        return returnItemId;
    }

    public void setReturnItemId(int returnItemId) {
        this.returnItemId = returnItemId;
    }

    public int getReturnItemCount() {
        return returnItemCount;
    }

    public void setReturnItemCount(int returnItemCount) {
        this.returnItemCount = returnItemCount;
    }

    public int getReturnItemEnchant() {
        return returnItemEnchant;
    }

    public void setReturnItemEnchant(int returnItemEnchant) {
        this.returnItemEnchant = returnItemEnchant;
    }

    public int getReturnItemBless() {
        return returnItemBless;
    }

    public void setReturnItemBless(int returnItemBless) {
        this.returnItemBless = returnItemBless;
    }
}
