package ks.core.datatables.bugCheck;

public class BugCheck {
    private int itemObjectId;
    private String itemName;
    private int itemCount;
    private int itemBless;
    private int itemId;
    private int itemEnchant;

    public int getItemEnchant() {
        return itemEnchant;
    }

    public void setItemEnchant(int itemEnchant) {
        this.itemEnchant = itemEnchant;
    }

    public int getItemObjectId() {
        return itemObjectId;
    }

    public void setItemObjectId(int itemObjectId) {
        this.itemObjectId = itemObjectId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public int getItemCount() {
        return itemCount;
    }

    public void setItemCount(int itemCount) {
        this.itemCount = itemCount;
    }

    public int getItemBless() {
        return itemBless;
    }

    public void setItemBless(int itemBless) {
        this.itemBless = itemBless;
    }

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }
}
