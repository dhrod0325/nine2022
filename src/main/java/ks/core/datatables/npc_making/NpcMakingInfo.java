package ks.core.datatables.npc_making;

public class NpcMakingInfo {
    private int npcId;
    private String ment1;
    private String ment2;

    public int getNpcId() {
        return npcId;
    }

    public void setNpcId(int npcId) {
        this.npcId = npcId;
    }

    public String getMent1() {
        return ment1;
    }

    public void setMent1(String ment1) {
        this.ment1 = ment1;
    }

    public String getMent2() {
        return ment2;
    }

    public void setMent2(String ment2) {
        this.ment2 = ment2;
    }
}
