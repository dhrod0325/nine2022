package ks.core.datatables.polyCard;

public class L1PolyCard {
    private int itemId;
    private String itemName;
    private int polyGrade;
    private int polyId;
    private int img;
    private int imgOff;

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public int getPolyGrade() {
        return polyGrade;
    }

    public void setPolyGrade(int polyGrade) {
        this.polyGrade = polyGrade;
    }

    public int getPolyId() {
        return polyId;
    }

    public void setPolyId(int polyId) {
        this.polyId = polyId;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public int getImgOff() {
        return imgOff;
    }

    public void setImgOff(int imgOff) {
        this.imgOff = imgOff;
    }
}
